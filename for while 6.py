#Escribe un programa que imprima la tabla de multiplicar de un número 
#dado por el usuario, desde el 1 hasta el 10, utilizando un bucle for.
branyu= int(input('ingrese un numero positivo'))
for murillo in range(1,11):
    print('valor: {0} x {1} = {2}'.format(branyu, murillo, (branyu * murillo)))
    
